const params = new URLSearchParams(window.location.search);

const id = params.get("id");

const regulation = regulationsData[id];

if(!regulation){

    document.getElementById("pageTitle").textContent = "Regulamento não encontrado";

    document.getElementById("pageDescription").textContent = "";

}
else{

    document.getElementById("pageTitle").textContent = regulation.title;

    document.getElementById("pageDescription").textContent = regulation.description;

}

const container = document.getElementById("regulationContent");

if(regulation){

    regulation.sections.forEach(section=>{

        container.innerHTML += `

            <div class="rule-card">

                <h3>${section.heading}</h3>

                <ul>

                    ${section.items.map(item=>`<li>${item}</li>`).join("")}

                </ul>

            </div>

        `;

    });

}